#!/bin/bash
# ============================================================
# build_mac.sh - macOS에서 실행파일(.app) 빌드 스크립트
# ============================================================
# 사용법: 터미널에서 아래 명령어 실행
#   chmod +x build_mac.sh   (최초 1회만)
#   ./build_mac.sh
# ============================================================

echo "================================================="
echo " JEUS 로그 분석기 macOS 빌드 시작"
echo "================================================="
echo ""

# 필요한 라이브러리 설치
echo "[1단계] 필요한 라이브러리 설치 중..."
pip3 install pyinstaller openpyxl
echo ""

# PyInstaller로 단일 실행파일 빌드
# --onefile   : 모든 파일을 하나로 합칩니다
# --windowed  : 터미널 창 없이 GUI만 보이게 합니다 (macOS .app 번들)
# --name      : 생성될 실행파일 이름
echo "[2단계] 실행파일 빌드 중... (수 분 소요될 수 있습니다)"
pyinstaller \
    --onefile \
    --windowed \
    --name "JEUS로그분석기" \
    --hidden-import=openpyxl \
    --hidden-import=openpyxl.styles \
    --hidden-import=openpyxl.utils \
    main.py

echo ""
if [ $? -eq 0 ]; then
    echo "================================================="
    echo " 빌드 성공!"
    echo " dist 폴더에서 JEUS로그분석기 파일을 확인하세요."
    echo " (.app 번들 또는 단일 실행파일로 생성됩니다)"
    echo "================================================="
else
    echo "================================================="
    echo " 빌드 실패! 위의 오류 메시지를 확인해주세요."
    echo "================================================="
fi
